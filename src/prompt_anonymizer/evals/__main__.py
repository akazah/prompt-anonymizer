"""Run the evaluation harness: ``uv run python -m prompt_anonymizer.evals``.

Generates the seeded golden set, runs the anonymizer, writes span-level
metrics to ``docs/EVAL.md`` and exports the golden set as JSON to
``tests/golden/`` for parity testing with the TypeScript core.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

from prompt_anonymizer.core import PromptAnonymizer
from prompt_anonymizer.evals.generate import generate_cases
from prompt_anonymizer.evals.metrics import EvalReport, evaluate_cases

HEADER = """# Accuracy (span-level, synthetic golden set)

Generated by `uv run python -m prompt_anonymizer.evals`. Cases are seeded
Faker documents (request / minutes / inquiry genres) with ground-truth spans.
Detection is best-effort; these numbers exist to catch regressions, not to
promise recall on real-world text.

| Language | Entity | Precision | Recall | F1 | Support |
|---|---|---|---|---|---|
"""


def main() -> None:
    parser = argparse.ArgumentParser(description="prompt-anonymizer evaluation harness")
    parser.add_argument("--cases", type=int, default=200, help="cases per language")
    parser.add_argument("--languages", nargs="+", default=["ja", "en"])
    parser.add_argument("--model-size", default="sm", choices=["sm", "lg"])
    parser.add_argument("--output", type=Path, default=Path("docs/EVAL.md"))
    parser.add_argument("--golden-dir", type=Path, default=Path("tests/golden"))
    args = parser.parse_args()

    reports: list[EvalReport] = []
    args.golden_dir.mkdir(parents=True, exist_ok=True)

    for language in args.languages:
        cases = generate_cases(language, count=args.cases)
        golden_path = args.golden_dir / f"golden_{language}.json"
        golden_path.write_text(
            json.dumps([c.to_dict() for c in cases], ensure_ascii=False, indent=1),
            encoding="utf-8",
        )
        print(f"[{language}] exported {len(cases)} cases -> {golden_path}")

        pa = PromptAnonymizer(languages=[language], model_size=args.model_size)
        predictions = []
        for i, case in enumerate(cases):
            predictions.append(pa.anonymize(case.text, language=language).entities)
            if (i + 1) % 50 == 0:
                print(f"[{language}] analyzed {i + 1}/{len(cases)}")
        reports.append(evaluate_cases(cases, predictions))

    args.output.parent.mkdir(parents=True, exist_ok=True)
    lines = [HEADER]
    for report in reports:
        lines.extend(row + "\n" for row in report.to_markdown_rows())
    lines.append(
        f"\nModel size: `{args.model_size}` / cases per language: {args.cases} (seed fixed).\n"
    )
    args.output.write_text("".join(lines), encoding="utf-8")
    print(f"wrote {args.output}")


if __name__ == "__main__":
    main()
